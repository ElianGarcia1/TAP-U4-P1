    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int rengloSeleccionado = jTable1.getSelectedRow();
        try{
            rengloSeleccionado = jTable1.getSelectedRow();
            if(rengloSeleccionado == -1){   
                JOptionPane.showMessageDialog(this, "Error debes seleccionar un renglon");
        }
        }catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Error debes seleccionar un renglon");
        }
        String idBorrar = tabla.getValueAt(rengloSeleccionado, 0).toString();
        boolean resultadoEliminar =mySQL.borrarArt(idBorrar);
        if (resultadoEliminar) {
            actualizarJTable();
            JOptionPane.showMessageDialog(this, "Exito dato eliminado");
        }else{
            JOptionPane.showMessageDialog(this, "Error dato no eliminado");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Productos productosInsertar = new Productos(jTextField2.getText(), Integer.parseInt(jTextField3.getText()), Integer.parseInt(jTextField4.getText()));
        boolean resultadoInsert = mySQL.insertar(productosInsertar);
        if(resultadoInsert){
            actualizarJTable();
            JOptionPane.showMessageDialog(this, "Exito, se insertaron los datos correctamente");
            jTextField2.setText("");
            jTextField3.setText("");
            jTextField4.setText("");
        }else{
            JOptionPane.showMessageDialog(this, "Error");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

